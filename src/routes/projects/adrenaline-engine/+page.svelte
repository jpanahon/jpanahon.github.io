    <h1>Technical Specifications</h1>
    <p>
        Now this is the part where I can totally nerd out about my project. I created my own abstraction of the Vulkan
        API which was originally written in C, the reason why I used C++ instead of C is because I wanted to learn it,
        since it is a mix of low and high level programming suitable for these type of programs. I started learning
        Vulkan by reading the <a href="https://vulkan-tutorial.com"> Vulkan Tutorial by Alexander Overvoorde</a>, after
        completing the tutorial I went on to research about many organizational techniques because all the code in the
        tutorial was in one class which is not good.
    </p>
