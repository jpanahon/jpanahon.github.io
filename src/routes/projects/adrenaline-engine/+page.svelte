    <summary class="text-3xl">Back Story</summary>
    <p class="indent-10 text-xl">
        I have always been an avid gamer from the moment I got my hands on the wonderful game called Minecraft,
        kickstarting my video game addiction. I have spent probably 2,500 hours of my life playing video games, which is
        really bad but at least I was doing something fun.
    </p>
    <p class="indent-10 text-xl">
        When I learned how to program for the first time ever in Python, I had thought about creating a game from
        scratch as I could learn a lot about how computers work, and learn some neat programming tricks along the way. I
        researched a lot about game development and came to the conclusion I needed to use a game engine, so I picked
        the most popular one at the time which was Unity.
    </p>
    <p class="indent-10 text-xl">
        Back then I had an old MacBook Air that was around 7 years old at the time I started with 4 GB of RAM, which was
        not enough to run Unity properly without me dying of old age or in an explosion by the time the engine was
        finished loading, unfortunately Unreal Engine also suffered the same fate.
    </p>
    <p class="indent-10 text-xl">
        This led me to the only "logical" conclusion that was inspired by Bender Bending Rodriguez from the TV Show
        Futurama, "I'll build my own game engine, with blackjack and hookers!". This led to me learning C++ and the ins
        and outs of Vulkan, which was the only graphics API I was able to get running on my computer, as Apple
        deprecated OpenGL support since MacOS Mojave, Metal was so new that I could not find anything about setting it
        up, and DirectX is Windows only. It wasn't until later I learned about SFML and SDL that I eventually decided to
        make something in SFML which ended up being Norepinephrine.
    </p>
    <p class="indent-10 text-xl">
        The development of this project stagnated when I got admitted to the University of Windsor, pursuing a
        bachelor's degree in Computer Science. Thanks to some of the content I learned in class I was able to improve
        upon the codebase of this project whenever I had time to work on it.
    </p>
</details>

